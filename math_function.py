import math
x = int(input("enter num"))
y=4
print(math.sqrt(x))
print(math.trunc(x))	#Returns the truncated integer value of x
print(math.exp(x)	)#Returns e**x
print(math.expm1(x)	)#Returns e**x - 1
print(math.log(x)	)#Returns the logarithm of x to the base b (defaults to e)
print(math.log1p(x)	)#Returns the natural logarithm of 1+x
print(math.log2(x)	)#Returns the base-2 logarithm of x
print(math.log10(x)	)#Returns the base-10 logarithm of x
print(math.pow(x, y)	)#Returns x raised to the power y
print(math.sqrt(x)	)#Returns the square root of x
print(math.acos(x)	)#Returns the arc cosine of x
print(math.asin(x)	)#Returns the arc sine of x
print(math.atan(x)	)#Returns the arc tangent of x
print(math.atan2(y, x))	#Returns atan(y / x)
print(math.cos(x)	)#Returns the cosine of x
print(math.hypot(x, y))	#Returns the Euclidean norm, sqrt(x*x + y*y)
print(math.sin(x)	)#Returns the sine of x
print(math.tan(x)	)#Returns the tangent of x
print(math.degrees(x))	#Converts angle x from radians to degrees
print(math.radians(x))	#Converts angle x from degrees to radians
print(math.acosh(x)	)#Returns the inverse hyperbolic cosine of x
print(math.asinh(x)	)#Returns the inverse hyperbolic sine of x
print(math.atanh(x)	)#Returns the inverse hyperbolic tangent of x
print(math.cosh(x)	)#Returns the hyperbolic cosine of x
print(math.sinh(x)	)#Returns the hyperbolic cosine of x
print(math.tanh(x)	)#Returns the hyperbolic tangent of x
print(math.erf(x)	)#Returns the error function at x
print(math.erfc(x)	)#Returns the complementary error function at x
print(math.gamma(x)	)#Returns the Gamma function at x
print(math.lgamma(x)	)#Returns the natural logarithm of the absolute value of the Gamma function at x
